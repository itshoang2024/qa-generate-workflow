const path = require("node:path");

const fontPath = (fileName) => {
  const absolute = path.resolve(__dirname, "google", fileName);
  const normalized = absolute.replace(/\\/g, "/");
  if (normalized.startsWith("/")) {
    return normalized;
  }
  return normalized.replace(/^[A-Za-z]:/, "");
};

